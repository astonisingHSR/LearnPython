#数学运算符
print(1+1)
print(1-1)
print(1*1)
print(1/2)
print(11//2) #整除运算
print(11%2)
print(3**2) #幂运算
print(-9//4) # 结果为-3，意味整除是向下取整
print(9//-4) # 结果为-3，意味整除是向下取整

print(-9%4) #余数 = 被除数 - 除数*商 = -9 - 4*（-3） = 3
print(9%-4) #余数 = 被除数 - 除数*商 = 9 - -4*（-3） = -3